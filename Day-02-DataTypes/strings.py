text = ("Python Learning")

# Indexing
print(text[0])
print(text[-1])
print(text[-3])

# Slicing
print(text[0:9])
print(text[::-1])

# String methods
print(text.upper())
print(text.replace("Python", "Java"))
print(text.find('o'))

# String formatting
name = "Sowmya"
print(f"Welcome, {name}!")
print("Welcome, {}!".format(name))
