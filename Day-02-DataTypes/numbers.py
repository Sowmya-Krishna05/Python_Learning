import random
import math
a = 30
b = 4

print(a + b)
print(a / b)
print(a // b)
print(a % b)
print(a ** b)

# Type casting
print(int(4.6))
print(float(3.14))

# Math module
print(math.sqrt(16))
print(math.ceil(2.18))
print(math.floor(13.9))

# Random module
print(random.randint(1, 100))
print(random.choice([10, 20, 30]))
