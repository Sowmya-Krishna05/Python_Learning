# Math module
import math
print(math.sqrt(16))          
print(math.ceil(2.18))  #round a number upward to its nearest integer.    
print(math.floor(13.9))  #rounding down floating-point numbers to the nearest whole number.   

# Random module
import random 
print(random.randint(1, 100)) #generates a random integer between two specified values.
print(random.choice([10, 20, 30])) #to select a random element from a sequence.
